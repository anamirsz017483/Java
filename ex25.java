/******************************************************************************
Faça um programa, utilizando while, que mostre na tela os números de 0 a 100.

*******************************************************************************/
import java.util.Scanner; 
public class ex25 
{
	public static void main(String[] args) {
	    int i =0;
	    
	    while (i <= 100) {
	        System.out.println(i);
	        i++;
	    }
  }
}