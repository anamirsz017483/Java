/******************************************************************************
Escreva um programa em Java que imprima os números de 1 a 10 usando o laço for.
*******************************************************************************/
import java.util.Scanner; 
public class prova02
{
    public static void main(String[] args) {
    
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }
    }
}
